#include<stdio.h>

///////////////////////////////////////////////////////
//
//  Function Name : main
//  Input :         Integer Number
//  Output :        Square of the Number
//  Description :   Multiplies the number by itself
//                  and displays the result.
//
///////////////////////////////////////////////////////

int main()
{
    int Num = 0;
    int Ans = 0;

    ///////////////////////////////////////////////////
    // Input Section
    ///////////////////////////////////////////////////

    printf("Enter the Number : ");
    scanf("%d", &Num);

    ///////////////////////////////////////////////////
    // Calculation Section
    ///////////////////////////////////////////////////

    Ans = Num * Num;

    ///////////////////////////////////////////////////
    // Output Section
    ///////////////////////////////////////////////////

    printf("Square of %d = %d\n", Num, Ans);

    return 0;
}

///////////////////////////////////////////////////////
//
//  Sample Input :
//      Enter the Number : 6
//
//  Sample Output :
//      Square of 6 = 36
//
///////////////////////////////////////////////////////
